#include "lists.h"

/**
 * print_listint - Function that print the length of the node and also print
 * @h: Input value
 * Return: always true
 */

size_t print_listint(const listint_t *h)
{
	int count;

	count = 0;

	while (h != NULL)
	{
		printf("%d\n", h->n);
		count = count + 1;
		h = h->next;
	}

	return (count);
}
